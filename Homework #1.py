print('\nВведите любую строку, кроме пустого значения, например "Топинамбур": \n')
example = input()
while not example:
    print('\nВведите любую строку, кроме пустого значения: \n')
    example = input()
print('\n')
print('Первый символ:', example[0])
print('Последний символ:', example[-1])
print('Вторая половина строки:', example[len(example)//2::])
print('Обратная строка:', example[::-1])
print('Четные символы:', example[1::2])
print('\nПрактическое задание №1 "Строки и индексация строк" выполнено.')